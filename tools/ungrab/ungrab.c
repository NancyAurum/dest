int unrle(uint8_t *src, uint8_t *dst, int sz) {
  uint8_t *q;
  uint8_t b;
  int i;
  uint8_t *end = dst + sz;
  uint8_t *ps;
  uint8_t *s = src;

  do {
    do {
      ps = s;
      s = ps + 1;
      b = *ps;
    } while (b == 0x80);
    if (b < 0x80) {
      i = (uint8_t)(b + 1);
      do {
        q = s;
        s = s + 1;
        if (*q != 0) {
          *dst = *q;
        }
        dst = dst + 1;
        i = i - 1;
      } while (i != 0);
    } else {
      i = (uint8_t)((b ^ 0xff) + 2); //negate
      q = s;
      s = ps + 2;
      b = *q;
      if (b == 0) {
        dst = dst + i;
      }
      else {
        for (; i != 0; i = i - 1) {
          q = dst;
          dst = dst + 1;
          *q = b;
        }
      }
    }
  } while (dst < end);
  return s - src;
}


int is_txt(uint8_t *p, uint32_t size) {
  uint32_t i;
  if (!size) return 0;
  //if (p[size-1] != 0x1A) return 0;
  for (i = 0; i < size; i++) {
    if (p[i] >= 0x80) return 0;
    if (p[i] == 0x1A && i==size-1) continue;
    if (p[i] < 0x20 && p[i]!=0xd && p[i]!=0xa) return 0;
  }
  return 1;
}


